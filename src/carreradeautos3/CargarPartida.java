    private void CargarDatosComboBox(){
    
    String sDirectorio = System.getProperty("user.dir");
    File f = new File(sDirectorio);
    if (f.exists()){ 
        File[] ficheros = f.listFiles();
        for (int x=0;x<ficheros.length;x++){
            if(ficheros[x].getName().contains(".json")){
                Combo.addItem(ficheros[x].getName());
            }
        }
    }
    }
    
    private void CargarDatosDeJson(String JsonData) throws JSONException{
        String jsonData = readFile(JsonData);
        jobj = new JSONObject(jsonData);   
    }
    
    public static String readFile(String filename) {
    String result = "";
    try {
        BufferedReader br = new BufferedReader(new FileReader(filename));
        StringBuilder sb = new StringBuilder();
        String line = br.readLine();
        while (line != null) {
            sb.append(line);
            line = br.readLine();
        }
        result = sb.toString();
    } catch(Exception e) {
        e.printStackTrace();
    }
    return result;
}
    
